CREATE TABLE IF NOT EXISTS app_menu (
    id int4 GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    parent_id int4 NULL,
    name varchar(50) NOT NULL,
    description varchar(100) NULL,
    url varchar(100) NULL,
    sort int4 NOT NULL,
    "group" varchar(50) DEFAULT 'main'::character varying NOT NULL,
    icon varchar(50) NULL,
    active bool DEFAULT true NOT NULL,
    display bool DEFAULT true NOT NULL,
    created_by varchar(50) NULL,
    created_at timestamp DEFAULT CURRENT_TIMESTAMP,
    updated_by varchar(50) NULL,
    updated_at timestamp NULL,
    CONSTRAINT app_menu_pkey PRIMARY KEY (id),
    CONSTRAINT app_menu_parent_id_fkey FOREIGN KEY (parent_id) REFERENCES app_menu(id) ON DELETE SET NULL,
    CONSTRAINT app_menu_created_by_fkey FOREIGN KEY (created_by) REFERENCES app_user(id) ON DELETE SET NULL,
    CONSTRAINT app_menu_updated_by_fkey FOREIGN KEY (updated_by) REFERENCES app_user(id) ON DELETE SET NULL
);